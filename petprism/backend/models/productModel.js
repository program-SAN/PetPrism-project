import mongoose from "mongoose";

const reviewSchema = mongoose.Schema(
  {
    Name: { type: String, required: true },
    rating: { type: Number, required: true },
    comment: { type: String, required: true },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
  },
  {
    timestamps: true,
  }
);

const productSchema = mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
    Name: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    reviews: [reviewSchema],
    rating: {
      type: Number,
      required: true,
      default: 0,
    },
    numberofreviews: {
      type: Number,
      required: true,
      default: 0,
    },
    price: {
      type: Number,
      required: true,
      default: 0,
    },
    countInstock: {
      type: Number,
      required: true,
      default: 0,
    },
  },
  {
    timestamps: true, //The timestamps option is a feature in Mongoose that allows you to automatically add two fields, createdAt and updatedAt, to your schema.
  }
);
const Product = mongoose.model("Product", productSchema);

export default Product;
